const main = async () => {
    const [deployer] = await hre.ethers.getSigners();
    const accountBalance = await deployer.getBalance();
  
    console.log("Deploying contracts with account: ", deployer.address);
    console.log("Account balance: ", accountBalance.toString());
  
    const imageContractFactory = await hre.ethers.getContractFactory("Image");
    const imageContract = await imageContractFactory.deploy();
    await imageContract.deployed();
  
    console.log("Image Contract address: ", imageContract.address);
  };
  
  const runMain = async () => {
    try {
      await main();
      process.exit(0);
    } catch (error) {
      console.log(error);
      process.exit(1);
    }
  };
  
  runMain();